package com.enation.app.tradeease.core.model.sms;

import com.enation.framework.database.PrimaryKeyField;

public class VistUserCount {

	private Integer visit_id;//主键
	
	private String visit_time; //出入时间
	
	private String visit_message;//出入session信息
	
	private Integer visit_num;//单天访问量
	
	private Integer visit_onlinenum;//在线人数
	
	@PrimaryKeyField
	public Integer getVisit_id() {
		return visit_id;
	}
	
	public void setVisit_id(Integer visit_id) {
		this.visit_id = visit_id;
	}

	

	public String getVisit_time() {
		return visit_time;
	}
	public void setVisit_time(String visit_time) {
		this.visit_time = visit_time;
	}
	public String getVisit_message() {
		return visit_message;
	}

	public void setVisit_message(String visit_message) {
		this.visit_message = visit_message;
	}

	public Integer getVisit_num() {
		return visit_num;
	}

	public void setVisit_num(Integer visit_num) {
		this.visit_num = visit_num;
	}

	public Integer getVisit_onlinenum() {
		return visit_onlinenum;
	}

	public void setVisit_onlinenum(Integer visit_onlinenum) {
		this.visit_onlinenum = visit_onlinenum;
	}
	
	
}
