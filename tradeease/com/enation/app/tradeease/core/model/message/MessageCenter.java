package com.enation.app.tradeease.core.model.message;

import java.io.Serializable;

/**
 * 站内信
 * 
 * @author yanpeng 2015-6-9 下午4:48:38
 * 
 */

@SuppressWarnings("serial")
public class MessageCenter implements Serializable {
	private Integer message_id;
	private Integer from_member_id;// 发送者ID
	private Integer goods_id;
	private Integer order_id;
	private String message_title;
	private String message_type;
	private String message_text;
	private String describe_text;
	private long send_date;

	public Integer getMessage_id() {
		return message_id;
	}

	public void setMessage_id(Integer message_id) {
		this.message_id = message_id;
	}

	public Integer getFrom_member_id() {
		return from_member_id;
	}

	public void setFrom_member_id(Integer from_member_id) {
		this.from_member_id = from_member_id;
	}

	public Integer getGoods_id() {
		return goods_id;
	}

	public void setGoods_id(Integer goods_id) {
		this.goods_id = goods_id;
	}

	public Integer getOrder_id() {
		return order_id;
	}

	public void setOrder_id(Integer order_id) {
		this.order_id = order_id;
	}

	public String getMessage_title() {
		return message_title;
	}

	public void setMessage_title(String message_title) {
		this.message_title = message_title;
	}

	public String getMessage_type() {
		return message_type;
	}

	public void setMessage_type(String message_type) {
		this.message_type = message_type;
	}

	public String getMessage_text() {
		return message_text;
	}

	public void setMessage_text(String message_text) {
		this.message_text = message_text;
	}

	public String getDescribe_text() {
		return describe_text;
	}

	public void setDescribe_text(String describe_text) {
		this.describe_text = describe_text;
	}

	public long getSend_date() {
		return send_date;
	}

	public void setSend_date(long send_date) {
		this.send_date = send_date;
	}

}
