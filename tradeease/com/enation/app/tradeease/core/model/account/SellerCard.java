package com.enation.app.tradeease.core.model.account;

import java.io.Serializable;

/**
 * 卖家账户明细
 * 
 * @author jfw 
 * 
 */

@SuppressWarnings("serial")
public class SellerCard implements Serializable{
	
	private Integer id; //主键
	private String  login_name;//注册邮箱
	private Integer store_id;//店铺
	private String store_name;//店铺名称
	private String card_holder;//持卡人姓名
	private String card_num;//银行卡号
	private Integer is_enable;//银行卡状态(1,启用，0，禁用)
	private String check_description;//备用
	private Long check_time;//启用时间
	private Integer status;//审核状态（0，待审核，1，审核通过，2，审核不通过）
	private String bank_name;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getLogin_name() {
		return login_name;
	}
	public void setLogin_name(String login_name) {
		this.login_name = login_name;
	}
	public Integer getStore_id() {
		return store_id;
	}
	public void setStore_id(Integer store_id) {
		this.store_id = store_id;
	}
	public String getStore_name() {
		return store_name;
	}
	public void setStore_name(String store_name) {
		this.store_name = store_name;
	}
	public String getCard_holder() {
		return card_holder;
	}
	public void setCard_holder(String card_holder) {
		this.card_holder = card_holder;
	}
	public String getCard_num() {
		return card_num;
	}
	public void setCard_num(String card_num) {
		this.card_num = card_num;
	}
	public Integer getIs_enable() {
		return is_enable;
	}
	public void setIs_enable(Integer is_enable) {
		this.is_enable = is_enable;
	}
	public String getCheck_description() {
		return check_description;
	}
	public void setCheck_description(String check_description) {
		this.check_description = check_description;
	}
	public Long getCheck_time() {
		return check_time;
	}
	public void setCheck_time(Long check_time) {
		this.check_time = check_time;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public String getBank_name() {
		return bank_name;
	}
	public void setBank_name(String bank_name) {
		this.bank_name = bank_name;
	}
	
}
