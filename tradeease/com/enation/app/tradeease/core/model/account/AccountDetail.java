package com.enation.app.tradeease.core.model.account;

import java.io.Serializable;

/**
 * 卖家账户明细
 * 
 * @author jfw 
 * 
 */

@SuppressWarnings("serial")
public class AccountDetail implements Serializable{
	
	private Integer rec_id; //主键
	private String  unique_id;//交易流水号
	private Integer member_id;//商户编号
	private String member_name;//商户名称
	private Integer store_id;
	private String store_name;
	private Integer type;//交易类型(1,保证金，2，放款，3，体现，4，佣金，5，手续费)
	private Integer amount_type;//账户名称---账户类型（平台账户1，保证金账户2）
	private Double income_amount;//收入金额
	private Double outlay_amount;//支出金额
	private Double balance;//支出金额平台账户余额（平台账户中的---最后一笔交易余额应等于账户余额）
	private Double credit_balance;//保证金账户余额
	private Integer status;//状态(预留字段,目前只有正常状态)
	private Integer tx_number;//交易序列号（对同一个商户的交易排序）
	private String note;//备注
	private Long create_time;
	private Long last_time;
	private Integer oper_id;//操作员id 默认后台系统管理员---0
	private String oper_name;//操作员姓名 默认后台系统管理员---admin
	public Integer getRec_id() {
		return rec_id;
	}
	public void setRec_id(Integer rec_id) {
		this.rec_id = rec_id;
	}
	public String getUnique_id() {
		return unique_id;
	}
	public void setUnique_id(String unique_id) {
		this.unique_id = unique_id;
	}
	public Integer getMember_id() {
		return member_id;
	}
	public void setMember_id(Integer member_id) {
		this.member_id = member_id;
	}
	public String getMember_name() {
		return member_name;
	}
	public void setMember_name(String member_name) {
		this.member_name = member_name;
	}
	public Integer getStore_id() {
		return store_id;
	}
	public void setStore_id(Integer store_id) {
		this.store_id = store_id;
	}
	public String getStore_name() {
		return store_name;
	}
	public void setStore_name(String store_name) {
		this.store_name = store_name;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	public Integer getAmount_type() {
		return amount_type;
	}
	public void setAmount_type(Integer amount_type) {
		this.amount_type = amount_type;
	}
	public Double getIncome_amount() {
		return income_amount;
	}
	public void setIncome_amount(Double income_amount) {
		this.income_amount = income_amount;
	}
	public Double getOutlay_amount() {
		return outlay_amount;
	}
	public void setOutlay_amount(Double outlay_amount) {
		this.outlay_amount = outlay_amount;
	}
	public Double getBalance() {
		return balance;
	}
	public void setBalance(Double balance) {
		this.balance = balance;
	}
	public Double getCredit_balance() {
		return credit_balance;
	}
	public void setCredit_balance(Double credit_balance) {
		this.credit_balance = credit_balance;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public Integer getTx_number() {
		return tx_number;
	}
	public void setTx_number(Integer tx_number) {
		this.tx_number = tx_number;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	public Long getCreate_time() {
		return create_time;
	}
	public void setCreate_time(Long create_time) {
		this.create_time = create_time;
	}
	public Long getLast_time() {
		return last_time;
	}
	public void setLast_time(Long last_time) {
		this.last_time = last_time;
	}
	public Integer getOper_id() {
		return oper_id;
	}
	public void setOper_id(Integer oper_id) {
		this.oper_id = oper_id;
	}
	public String getOper_name() {
		return oper_name;
	}
	public void setOper_name(String oper_name) {
		this.oper_name = oper_name;
	}
	

	
}
