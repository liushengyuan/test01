package com.enation.app.shop.core.model;

import com.enation.framework.database.DynamicField;
import com.enation.framework.database.PrimaryKeyField;

/**
 * 推荐商品实体
 * @author ljs
 *2010-3-23下午03:34:04
 */
public class TeamProduct implements java.io.Serializable {
	
	private Integer id;//id
	private Integer team_id;//推荐类Team的id
	private Integer product_id;//推荐类Team关联的商品Id
	private String product_image;//图片地址
	private Integer product_index;//排序序列
	
	@PrimaryKeyField
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getTeam_id() {
		return team_id;
	}
	public void setTeam_id(Integer team_id) {
		this.team_id = team_id;
	}
	public Integer getProduct_id() {
		return product_id;
	}
	public void setProduct_id(Integer product_id) {
		this.product_id = product_id;
	}
	public String getProduct_image() {
		return product_image;
	}
	public void setProduct_image(String product_image) {
		this.product_image = product_image;
	}
	public Integer getProduct_index() {
		return product_index;
	}
	public void setProduct_index(Integer product_index) {
		this.product_index = product_index;
	}
	
	


}
