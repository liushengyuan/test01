package com.enation.app.shop.core.model;

/**
 * 商品详情保存运费等信息模板
 * 
 * @author Administrator
 * 
 */
public class GoodLogisDetail {
	private Integer id;
	private String session_id;
	private Integer freight_id;
	private Integer goods_id;
	private Double sendprice;// 运费
	private Integer store_id;
	private String currency; // 币种 RMB和卢布
	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getSession_id() {
		return session_id;
	}

	public void setSession_id(String session_id) {
		this.session_id = session_id;
	}

	public Integer getFreight_id() {
		return freight_id;
	}

	public void setFreight_id(Integer freight_id) {
		this.freight_id = freight_id;
	}

	public Integer getGoods_id() {
		return goods_id;
	}

	public void setGoods_id(Integer goods_id) {
		this.goods_id = goods_id;
	}

	public Double getSendprice() {
		return sendprice;
	}

	public void setSendprice(Double sendprice) {
		this.sendprice = sendprice;
	}

	public Integer getStore_id() {
		return store_id;
	}

	public void setStore_id(Integer store_id) {
		this.store_id = store_id;
	}

	public String getCurrency() {
		return currency;
	}

	public void setCurrency(String currency) {
		this.currency = currency;
	}


}
