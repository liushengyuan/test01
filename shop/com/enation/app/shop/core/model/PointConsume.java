public class PointConsume implements java.io.Serializable {
	
	private static final long serialVersionUID = 1L;
	
	//积分消费Id
	private Integer consume_id;
	//积分数量
	private Integer consume_num;
	//币种
	private String consume_currency;
	//抵消金额
	private Double consume_amount;
	
	public Integer getConsume_id() {
		return consume_id;
	}
	public void setConsume_id(Integer consume_id) {
		this.consume_id = consume_id;
	}
	public Integer getConsume_num() {
		return consume_num;
	}
	public void setConsume_num(Integer consume_num) {
		this.consume_num = consume_num;
	}
	public String getConsume_currency() {
		return consume_currency;
	}
	public void setConsume_currency(String consume_currency) {
		this.consume_currency = consume_currency;
	}
	public Double getConsume_amount() {
		return consume_amount;
	}
	public void setConsume_amount(Double consume_amount) {
		this.consume_amount = consume_amount;
	}
	
	
}