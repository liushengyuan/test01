package com.enation.app.b2b2c.core.model;

import java.io.Serializable;

/**
 * 国家表
 * @author WKZ
 * @date 2015-9-19 上午9:02:58
 */
public class Country implements Serializable{

	private static final long serialVersionUID = -3611072385708540242L;
	
	private Integer country_id;//国家ID
	private String country_name;//国家名称
	private String country_code;//国家简写代码
	
	
	public Integer getCountry_id() {
		return country_id;
	}
	public void setCountry_id(Integer country_id) {
		this.country_id = country_id;
	}
	public String getCountry_name() {
		return country_name;
	}
	public void setCountry_name(String country_name) {
		this.country_name = country_name;
	}
	public String getCountry_code() {
		return country_code;
	}
	public void setCountry_code(String country_code) {
		this.country_code = country_code;
	}
	
	
	
}
