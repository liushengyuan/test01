package com.enation.app.base.core.model;

/**
 * 首页大屏广告
 * @author WKZ
 * @date 2015-9-2 上午10:46:21
 */
public class AdIndex {
	
	private Integer adindex_id;
	private Integer adindex_disable;//状态  1启用 2停用
	private String adindex_url;//图片地址
	private String outer_css;//大图的CSS
	private String close_css;//关闭按钮的CSS
	private String jump_css;//跳转按钮的CSS
	
	public Integer getAdindex_id() {
		return adindex_id;
	}
	public void setAdindex_id(Integer adindex_id) {
		this.adindex_id = adindex_id;
	}
	public String getAdindex_url() {
		return adindex_url;
	}
	public void setAdindex_url(String adindex_url) {
		this.adindex_url = adindex_url;
	}
	public Integer getAdindex_disable() {
		return adindex_disable;
	}
	public void setAdindex_disable(Integer adindex_disable) {
		this.adindex_disable = adindex_disable;
	}
	public String getOuter_css() {
		return outer_css;
	}
	public void setOuter_css(String outer_css) {
		this.outer_css = outer_css;
	}
	public String getClose_css() {
		return close_css;
	}
	public void setClose_css(String close_css) {
		this.close_css = close_css;
	}
	public String getJump_css() {
		return jump_css;
	}
	public void setJump_css(String jump_css) {
		this.jump_css = jump_css;
	}


}
